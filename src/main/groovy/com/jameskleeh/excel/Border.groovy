package com.jameskleeh.excel

/**
 * Border enum mapped to {@link org.apache.poi.ss.usermodel.BorderStyle}
 *
 * @author James Kleeh
 * @since 1.0.0
 */
enum Border {
    /**
     * No border
     */

    NONE,

    /**
     * Thin border
     */

    THIN,

    /**
     * Medium border
     */

    MEDIUM,

    /**
     * dash border
     */

    DASHED,

    /**
     * dot border
     */

    DOTTED,

    /**
     * Thick border
     */

    THICK,

    /**
     * double-line border
     */

    DOUBLE,

    /**
     * hair-line border
     */

    HAIR,

    /**
     * Medium dashed border
     */

    MEDIUM_DASHED,

    /**
     * dash-dot border
     */

    DASH_DOT,

    /**
     * medium dash-dot border
     */

    MEDIUM_DASH_DOT,

    /**
     * dash-dot-dot border
     */

    DASH_DOT_DOT,

    /**
     * medium dash-dot-dot border
     */

    MEDIUM_DASH_DOT_DOTC,

    /**
     * slanted dash-dot border
     */

    SLANTED_DASH_DOT
}
