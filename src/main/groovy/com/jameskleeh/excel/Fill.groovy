package com.jameskleeh.excel

/**
 * Fill enum mapped to {@link org.apache.poi.ss.usermodel.FillPatternType}
 *
 * @author James Kleeh
 * @since 1.0.0
 */
enum Fill  {

    /**  No background */
    NO_FILL,

    /**  Solidly filled */
    SOLID_FOREGROUND,

    /**  Small fine dots */
    FINE_DOTS,

    /**  Wide dots */
    ALT_BARS,

    /**  Sparse dots */
    SPARSE_DOTS,

    /**  Thick horizontal bands */
    THICK_HORZ_BANDS,

    /**  Thick vertical bands */
    THICK_VERT_BANDS,

    /**  Thick backward facing diagonals */
    THICK_BACKWARD_DIAG,

    /**  Thick forward facing diagonals */
    THICK_FORWARD_DIAG,

    /**  Large spots */
    BIG_SPOTS,

    /**  Brick-like layout */
    BRICKS,

    /**  Thin horizontal bands */
    THIN_HORZ_BANDS,

    /**  Thin vertical bands */
    THIN_VERT_BANDS,

    /**  Thin backward diagonal */
    THIN_BACKWARD_DIAG,

    /**  Thin forward diagonal */
    THIN_FORWARD_DIAG,

    /**  Squares */
    SQUARES,

    /**  Diamonds */
    DIAMONDS,

    /**  Less Dots */
    LESS_DOTS,

    /**  Least Dots */
    LEAST_DOTS

}
